import java.util.HashSet;
import java.util.Set;

public class HashSetCheck {

	public static void main(String[] args) {

		Set<Integer> hashSet = new HashSet<>();

		hashSet.add(1);
		hashSet.add(29);
		hashSet.add(31);
		hashSet.add(40);

		System.out.println("HashSetCheck.main()...hashSet:" + hashSet);
		

	}

}
